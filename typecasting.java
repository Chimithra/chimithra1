package com.company;
import java.lang.*;


class typecasting
{
    public static void main(String args[])
    {
        int a=10;
        double b = a,d=25.5321;
        int c=(int)d;
        System.out.println("int :"+a);
        System.out.println("double :"+b);
        System.out.println("double :"+d);
        System.out.println("int :"+c);
    }
}